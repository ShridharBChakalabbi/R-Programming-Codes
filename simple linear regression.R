x <- c(17,13,12,15,16,14,16,16,18,19)
y <- c(94,73,59,80,93,85,66,79,77,91)
relation <- lm(y~x)
print(relation)
print(summary(relation))
a <- data.frame(x=15)
result <- predict(relation,a)
print(result)
plot(y,x,col = 'green',main = 'SIMPLE linear regression',abline(lm(x~y)),cex = 1,pch = 16,xlab = 'X',ylab = 'Y')
